const Particle = ({ mass, position, myref, index }) => {
  const [particle, api] = useParticle(() => ({
    mass,
    position,
    linearDamping: 0.2,
  }));

  if (myref) myref.current = { particle, api };
  //posi={[x,y,z]}
  useEffect(() => {
    const unsub = api.position.subscribe((pos) => {
      position[0] = pos[0];
      position[1] = pos[1];
      position[2] = pos[2];
      //console.log("position sub: ", pos);
    });
    myref.current = { particle, api, position, index };
    return () => unsub();
  }, [api, position, particle, myref]);

  return null;
};

const Stitch = ({ p1, p2, distance }) => {
  const lineRef = useRef();

  useDistanceConstraint(p1.current.particle, p2.current.particle, {
    distance,
  });

  useFrame(() => {
    if (!lineRef.current || !p1.current || !p2.current) return;

    const pos1 = p1.current.position;
    const pos2 = p2.current.position;

    lineRef.current.geometry.setFromPoints([
      new three.Vector3(pos1[0], pos1[1], pos1[2]),
      new three.Vector3(pos2[0], pos2[1], pos2[2]),
    ]);
  });

  return null;
};

